    function getColorByStr(s: string): number {
        //颜色 1 (白)
        if (s == '1') {
            return 0xFFFF
            //颜色 2 (红)
        } else if (s == '2') {
            return 0x001F
            //颜色 3 (粉红色)
        } else if (s == '3') {
            return 0xC49F
            //颜色 4 (橙)
        } else if (s == '4') {
            return 0x341F
            //颜色 5 (黄)
        } else if (s == '5') {
            return 0x07FF
            //颜色 6 (teal)
        } else if (s == '6') {
            return 0xA4E4
            //颜色 7 (绿)
        } else if (s == '7') {
            return 0x56EF
            //颜色 8 (蓝)
        } else if (s == '8') {
            return 0xA9E0
            //颜色 9 (浅蓝色)
        } else if (s == '9') {
            return 0xFF90
            //颜色 10 (紫)
        } else if (s == 'a') {
            return 0xC171
            //颜色 11 (淡紫色)
        } else if (s == 'b') {
            return 0x9C14
            //颜色 12 (dark purple)
        } else if (s == 'c') {
            return 0x6A0B
            //颜色 13 (tan)
        } else if (s == 'd') {
            return 0xC67C
            //颜色 14 (棕色)
        } else if (s == 'e') {
            return 0x3A32
            //颜色 15 (黑色)
        } else if (s == 'f') {
            return 0x0000
            //颜色 0 (透明度)
        } else {
            return 0xCE59
        }
    }
    /**
     * 将多行地图字符串转换为 行x列 二维字符数组
     * @param str 原始多行字符串
     * @returns 二维数组：array[行][列] = 字符
     */
    function convertToGrid(str: string): string[][] {
        return str
            .split('\n') // 按换行符分割成每一行
            .filter(line => line.trim() !== '') // 过滤空行、空白行（去掉首尾空行）
            .map(line => line.split('')); // 每一行拆分成单个字符的数组
    }
    //% subcategory="tft"
    //% group='tft彩屏'
    //% block="tft彩屏显示图片 $im 在位置x: $xStart, y $yStart"
    export function tftShowArcade(im: TftArcade, xStart: number, yStart:number) {
        let imgStr = ""
        if (im == TftArcade.imTft1) {
            imgStr = imTft1
        } else if (im == TftArcade.imTft2) {
            imgStr = imTft2
        } else if (im == TftArcade.imTft3) {
            imgStr = imTft3
        } else if (im == TftArcade.imTft4) {
            imgStr = imTft4
        } else if (im == TftArcade.imTft5) {
            imgStr = imTft5
        }
        const grid = convertToGrid(imgStr);
        for (let y = 0; y < grid.length; y++) {
            for (let x = 0; x < grid[0].length; x++) {
                let pixel = grid[y][x]
                if (pixel) {
                    RBTFT18.drawPixel(x + xStart, y + yStart, getColorByStr(pixel))
                }
            }
        }
    }

